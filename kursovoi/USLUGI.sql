CREATE TABLE USLUGI_SKIDKI (
  ID NUMBER(10) PRIMARY KEY,
  NAZVANIE VARCHAR2(50),
  CENA NUMBER(10),
  SKIDKA NUMBER(10)
);

CREATE OR REPLACE TRIGGER TRG_USLUGI_SKIDKI
BEFORE INSERT ON USLUGI_SKIDKI
FOR EACH ROW
DECLARE
  V_COUNT NUMBER(10);
BEGIN
  SELECT COUNT(*) INTO V_COUNT FROM USLUGI_SKIDKI WHERE ID = :NEW.ID;
  IF MOD(V_COUNT, 3) = 0 THEN
    :NEW.SKIDKA := :NEW.CENA * 0.2;
  ELSE
    :NEW.SKIDKA := 0;
  END IF;
END;