CREATE TABLE KLIENTS
(
ID_KLIENTS NUMBER GENERATED BY DEFAULT AS IDENTITY,
ID4_ZAKAZA NUMBER(6,0),
ID4_DOKUMENT NUMBER(6,0),
TELEPHONE_KLIENTA VARCHAR2 (30),
ADRES_KLIENTA VARCHAR2 (140),
CONSTRAINT KLIENTS_PK PRIMARY KEY (ID_KLIENTS) ENABLE,
CONSTRAINT KLIENTS_FK FOREIGN KEY (ID4_ZAKAZA) REFERENCES DOKUMENT(ID_ZAKAZA),
CONSTRAINT KLIENTS_FK1 FOREIGN KEY (ID4_DOKUMENT) REFERENCES DOKUMENT(ID_DOKUMENTA)
);
/
CREATE OR REPLACE TRIGGER BI_KLIENTS
before insert on KLIENTS
for each row
begin
if:NEW. ID is null then
select KLIENTS_SEQ.nextval into:NEW. ID from sys.dual;
end if;
end;
/
ALTER TRIGGER BI_KLIENTS ENABLE;
SELECT * FROM KLIENTS;
drop table KLIENTS;
